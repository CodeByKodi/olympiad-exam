function buildNso(grade: (typeof NSO_GRADES)[number], set: 1 | 2 | 3): MockExam {
  const packId = `nso-mock-g${grade}-s0${set}`;
  const sections = normalizeMockSectionsToTarget(nsoSections(grade, set), 'NSO', grade, set, packId);
  return finalizeMockExam({
    id: packId,
    title: `NSO Class ${grade} Mock Paper ${set}`,
    exam_type: 'mock',
    exam_code: 'NSO',
    class_name: `Class ${grade}`,
    subject_name: 'Science',
    pattern_type: set === 3 ? 'full-length' : 'olympiad-standard',
    difficulty_level: set === 1 ? 'easy' : set === 2 ? 'medium' : 'hard',
    duration_minutes: set === 1 ? 45 : set === 2 ? 50 : 55,
    sections,
    instructions: nsoInstructions(set),
    tags: ['nso', `class${grade}`, 'mock', `set-${set}`, 'achievers'],
    source: 'NSO-style internal mock bank aligned to syllabus topic names',
  });
}

export const nsoMockExams: MockExam[] = NSO_GRADES.flatMap((g) =>
  ([1, 2, 3] as const).map((s) => buildNso(g, s)),
);
